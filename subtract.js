let a = 10; 
let b = 20; 
  
// Subtract directlty  
console.log(a - b); // -10 
console.log(b - a); // 10  
  
// Subtract by using another variable 
let c = b - a; 
console.log(c); // 10 
  
// Subtract without any variable 
console.log(10 - 20); //-10 
console.log(20 - 10); //10 
console.log(20 - 10 - 10); //0
